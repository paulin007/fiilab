public class FlyWeight {

	private String message;

	/**
	 * @param message
	 */
	public FlyWeight(String message) {
		super();
		this.message = message;
	}

	/**
	 * @return the message
	 */
	public String getMessage() {
		return message;
	}
	
	
}
